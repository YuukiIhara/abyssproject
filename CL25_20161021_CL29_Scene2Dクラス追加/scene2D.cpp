CScene2D::CScene2D()
{
	m_pTexture = NULL;	
	m_pVtxBuff = NULL;	
	m_pos = D3DXVECTOR3( 0.0f, 0.0f, 0.0f );
}
//********************************************************************************************************************
// funcinfo：デストラクタ
//********************************************************************************************************************
CScene2D::~CScene2D()
{

}
//********************************************************************************************************************
// funcinfo：初期化処理
//********************************************************************************************************************
HRESULT CScene2D::Init(void)
{
	// デバイスの取得
	LPDIRECT3DDEVICE9 pDevice;
	pDevice = GetRenderer() -> GetDevice();
	
	// ポリゴンの位置を設定
	m_pos = D3DXVECTOR3( SCREEN_WIDTH / 2.0f, SCREEN_HEIGHT / 2.0f, 0.0f );

	// テクスチャの生成
	D3DXCreateTextureFromFile(pDevice, "data/TEXTURE/player000.png ", &m_pTexture);

	// 頂点バッファの生成
	// オブジェクトの頂点バッファを作成
	if(FAILED(pDevice -> CreateVertexBuffer(sizeof(VERTEX_2D) * 4,
											D3DUSAGE_WRITEONLY,
											FVF_VERTEX_2D,
											D3DPOOL_MANAGED,
											&m_pVtxBuff,
											NULL)))
	{
		return E_FAIL;
	}

	// 頂点情報を設定
	{//頂点バッファの中身を埋める
		VERTEX_2D *pVtx;

		// 頂点データの範囲をロックし、頂点バッファへのポインタを取得
		m_pVtxBuff->Lock(0,0,(void**)&pVtx,0);

		// 頂点座標の設定
		pVtx[0].pos = D3DXVECTOR3(m_pos.x - 200.0f / 2, m_pos.y - 200.0f / 2, 0.0f);
		pVtx[1].pos = D3DXVECTOR3(m_pos.x + 200.0f / 2, m_pos.y - 200.0f / 2, 0.0f);
		pVtx[2].pos = D3DXVECTOR3(m_pos.x - 200.0f / 2, m_pos.y + 200.0f / 2, 0.0f);
		pVtx[3].pos = D3DXVECTOR3(m_pos.x + 200.0f / 2, m_pos.y + 200.0f / 2, 0.0f);
		// rhwの設定
		pVtx[0].rhw = 1.0f;
		pVtx[1].rhw = 1.0f;
		pVtx[2].rhw = 1.0f;
		pVtx[3].rhw = 1.0f;
		//頂点カラーの設定
		pVtx[0].col = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
		pVtx[1].col = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
		pVtx[2].col = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
		pVtx[3].col = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
		// テクスチャ座標指定
		pVtx[0].uv = D3DXVECTOR2( 0.0f, 0.0f );
		pVtx[1].uv = D3DXVECTOR2( 1.0f, 0.0f );
		pVtx[2].uv = D3DXVECTOR2( 0.0f, 1.0f );
		pVtx[3].uv = D3DXVECTOR2( 1.0f, 1.0f );
		
		// 頂点データをアンロックする
		m_pVtxBuff->Unlock();
	}

	return S_OK;
}
//********************************************************************************************************************
// funcinfo：終了処理
//********************************************************************************************************************
void CScene2D::Uninit(void)
{
	// 頂点バッファの破棄
	if(m_pVtxBuff != NULL)
	{	//バッファーの解放
		m_pVtxBuff -> Release();
		m_pVtxBuff = NULL;
	}

	// テクスチャの破棄
	if(m_pTexture != NULL)
	{	//バッファーの解放
		m_pTexture -> Release();
		m_pTexture = NULL;
	}
}
